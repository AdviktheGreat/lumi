"""Visual context report generator for the Lumi YOHAS pipeline.

Compiles a FinalReport (with optional embedded figures) into self-contained
HTML, PDF, or JSON outputs.  HTML is the single source of truth -- PDF is
rendered from the same HTML via xhtml2pdf.

Usage::

    from src.reports.generator import ReportGenerator, ReportFigure, FigureType

    gen = ReportGenerator()
    html = await gen.generate_html(report, figures=[...])
    pdf  = await gen.generate_pdf(report, figures=[...])
    json_str = gen.generate_json(report)
"""

from __future__ import annotations

import asyncio
import base64
import io
import json
import logging
from datetime import datetime, timezone
from enum import Enum
from pathlib import Path
from typing import Any

from pydantic import BaseModel, Field

